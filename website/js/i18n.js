export function getCopy(language, key) {
  const parts = key.split('.');
  let value = translations[language];
  for (const part of parts) {
    value = value?.[part];
  }
  if (value !== undefined) {
    return value;
  }
  let fallback = translations.en;
  for (const part of parts) {
    fallback = fallback?.[part];
  }
  return fallback ?? key;
}
